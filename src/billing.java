    private void searchProduct(){
        String query;
        if(txtSearchPID.getText().equals("ID"))
            query = "select * from product where product_name = '"+ txtSearchaPName.getText() +"'";
        else
            query = "select * from product where product_id = '"+ txtSearchPID.getText() +"'";

        try {
            Statement statement = connection.createStatement();
            ResultSet result = statement.executeQuery(query);
            if(result.next() ){
                if(result.getString(5).equals("Yes")){
                    txtSearchPID.setText(result.getString(1));
                    txtSearchPID.setForeground(Color.BLACK);
                    txtSearchaPName.setText(result.getString(2));
                    txtSearchaPName.setForeground(Color.BLACK);
                    lblRate.setText(result.getString(3));
                    lblDescription.setText(result.getString(4));
                    txtQty.requestFocus();
                    txtQty.setText("1");
                    txtQty.selectAll();
                }else{
                    setAlwaysOnTop(false);
                    JOptionPane.showMessageDialog(null, "Product is not active","",1);
                    setAlwaysOnTop(true);
                    if(txtSearchPID.getText().equals("ID"))
                        txtSearchaPName.requestFocus();
                    else
                        txtSearchPID.requestFocus();
                }
            }else{
                setAlwaysOnTop(false);
                JOptionPane.showMessageDialog(null, "Product does not exist","",1);
                setAlwaysOnTop(true);
                if(txtSearchPID.getText().equals("ID"))
                    txtSearchaPName.requestFocus();
                else
                    txtSearchPID.requestFocus();
            }
        } catch (SQLException ex) {
        }
    }
    
